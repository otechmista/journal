import { cleanContentHtml, cleanContentText } from './clean.js';

/**
 * @typedef {object} JournalMeta
 * @property {string} source_id
 * @property {'rss'|'scrape'} source_type
 * @property {string} ingested_at
 * @property {'ok'|'missing'|'error'} content_status
 * @property {string|null} [content_error]
 * @property {string|null} [article_snapshot]
 */

/**
 * @typedef {object} FeedItem
 * @property {string} id
 * @property {string} url
 * @property {string} title
 * @property {string} [summary]
 * @property {string} [content_text]
 * @property {string} [content_html]
 * @property {string} [image]
 * @property {string} [date_published]
 * @property {string} [date_modified]
 * @property {{ name: string }[]} [authors]
 * @property {JournalMeta} _journal
 */

/**
 * @param {Partial<FeedItem> & { url?: string, id?: string, title?: string }} raw
 * @param {{ source_id: string, source_type: 'rss'|'scrape' }} meta
 * @returns {FeedItem|null}
 */
export function normalizeItem(raw, meta) {
	const url = (raw.url || raw.id || '').trim();
	if (!url) return null;
	const title = (raw.title || '').trim() || url;
	const id = itemId(raw);
	const now = new Date().toISOString();

	const content_html = raw.content_html ? cleanContentHtml(raw.content_html) : undefined;
	const content_text = raw.content_text
		? cleanContentText(raw.content_text)
		: content_html
			? cleanContentText(content_html.replace(/<[^>]+>/g, ' '))
			: undefined;

	return {
		id,
		url,
		title,
		summary: raw.summary ? cleanContentText(raw.summary) : undefined,
		content_text: content_text || undefined,
		content_html: content_html || undefined,
		image: raw.image || undefined,
		date_published: raw.date_published || undefined,
		date_modified: raw.date_modified || undefined,
		authors: raw.authors || undefined,
		_journal: {
			source_id: meta.source_id,
			source_type: meta.source_type,
			ingested_at: now,
			content_status: raw._journal?.content_status || (content_html || content_text ? 'ok' : 'missing'),
			content_error: raw._journal?.content_error ?? null,
			article_snapshot: raw._journal?.article_snapshot ?? null
		}
	};
}

/**
 * Prefer guid/id, else absolute url.
 * @param {{ id?: string, url?: string, guid?: string }} raw
 */
export function itemId(raw) {
	const candidate = (raw.id || raw.guid || raw.url || '').trim();
	return candidate;
}

/**
 * @param {FeedItem[]} feeds
 * @returns {FeedItem[]}
 */
export function mergeFeeds(...feeds) {
	/** @type {Map<string, FeedItem>} */
	const map = new Map();
	for (const list of feeds) {
		for (const item of list) {
			const prev = map.get(item.id);
			if (!prev) {
				map.set(item.id, item);
				continue;
			}
			map.set(item.id, mergeItem(prev, item));
		}
	}
	return [...map.values()].sort(compareByDate);
}

/**
 * @param {FeedItem} a
 * @param {FeedItem} b
 */
function mergeItem(a, b) {
	const preferBContent =
		(b.content_html || b.content_text) &&
		(!a.content_html && !a.content_text || b._journal.content_status === 'ok');

	return {
		...a,
		...b,
		title: b.title || a.title,
		summary: b.summary || a.summary,
		content_html: preferBContent ? b.content_html || a.content_html : a.content_html || b.content_html,
		content_text: preferBContent ? b.content_text || a.content_text : a.content_text || b.content_text,
		image: b.image || a.image,
		date_published: b.date_published || a.date_published,
		date_modified: b.date_modified || a.date_modified,
		authors: b.authors || a.authors,
		_journal: {
			...a._journal,
			...b._journal,
			content_status:
				b._journal.content_status === 'ok'
					? 'ok'
					: a._journal.content_status === 'ok'
						? 'ok'
						: b._journal.content_status || a._journal.content_status,
			content_error:
				b._journal.content_status === 'ok'
					? null
					: b._journal.content_error ?? a._journal.content_error
		}
	};
}

/**
 * @param {FeedItem} a
 * @param {FeedItem} b
 */
export function compareByDate(a, b) {
	const da = a.date_published ? Date.parse(a.date_published) : NaN;
	const db = b.date_published ? Date.parse(b.date_published) : NaN;
	const aValid = !Number.isNaN(da);
	const bValid = !Number.isNaN(db);
	if (aValid && bValid) return db - da;
	if (aValid) return -1;
	if (bValid) return 1;
	return a.title.localeCompare(b.title);
}

/**
 * @param {string} title
 * @param {FeedItem[]} items
 */
export function buildFeedDocument(title, items) {
	return {
		version: 'https://jsonfeed.org/version/1.1',
		title,
		home_page_url: '/',
		feed_url: title === 'Journal' ? '/data/feeds/unified.json' : undefined,
		description: title === 'Journal' ? 'Unified local news feed' : `Feed snapshot for ${title}`,
		user_comment: 'Generated by Journal. Do not edit while a crawl job is running.',
		items
	};
}
